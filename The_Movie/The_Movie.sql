CREATE DATABASE THE_MOVIE

CREATE TABLE tbActor(
ACTOR_ID INTEGER PRIMARY KEY NOT NULL, 
ACTOR_FNAME VARCHAR(20),
ACTOR_INAME VARCHAR(20),
ACTOR_GENDER VARCHAR(1)
);

CREATE TABLE tbMovie(
MOV_ID INTEGER PRIMARY KEY NOT NULL,
MOV_TITLE VARCHAR(50),
MOV_YEAR INTEGER,
MOV_TIME INTEGER,
MOV_LANG VARCHAR(50),
MOV_DT_REL DATE,
MOV_REL_COUNTRY VARCHAR(5)
);

CREATE TABLE tbReviewer(
REV_ID INTEGER PRIMARY KEY NOT NULL, 
REV_NAME VARCHAR(30)
);

CREATE TABLE tbRating(
MOV_ID INTEGER REFERENCES tbMovie, 
REV_ID INTEGER REFERENCES tbReviewer,
REV_STARS INTEGER,
NUM_O_RATINGS INTEGER
);


CREATE TABLE tbMovie_cast(
ACTOR_ID INTEGER REFERENCES tbActor,
MOV_ID INTEGER,
ROLLE VARCHAR (30)
);

CREATE TABLE tbDirector(
DIR_ID INTEGER PRIMARY KEY NOT NULL,
DIR_FNAME VARCHAR(20),
DIR_INAME VARCHAR(20)
);

CREATE TABLE tbMovie_Direction(
DIR_ID INTEGER REFERENCES tbDirector,
MOV_ID INTEGER REFERENCES tbMovie
);

CREATE TABLE tbGenres(
GEN_ID INTEGER PRIMARY KEY NOT NULL,
GEN_TITLE VARCHAR(20)
);

CREATE TABLE Movie_Genres(
GEN_ID INTEGER  REFERENCES tbGenres,
MOV_ID INTEGER REFERENCES tbMovie
);

